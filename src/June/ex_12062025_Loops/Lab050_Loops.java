package June.ex_12062025_Loops;

public class Lab050_Loops {
    public static void main(String[] args) {

        // for(A; B; C)
        // A -> Initialization -> JVM What variable, where you are starting
        // B -> Condition -> When you want this to stop.
        // C -> Increment/ Decrement

        for (int i = 1; i<=10; i++){
            System.out.println(i);
        }

        // Interview Question 1

        for (int shruti = 1; shruti<8; shruti++) { // 1 to 7
            System.out.println(shruti); // 8<8
        }

        // Interview Question 2

        for (int _1 = 0; _1<= 10; _1++){
            System.out.println(_1);
        }
    }
}
