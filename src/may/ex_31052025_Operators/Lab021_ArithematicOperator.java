package may.ex_31052025_Operators;

public class Lab021_ArithematicOperator {
    public static void main(String[] args) {

        // Arithematic Operator
        // +,-,/,*

        int b = 10;
        int c = 2;

        System.out.println(b+c); // + -> binary -> 2 operands
        System.out.println(b-c);
        System.out.println(b*c);
        System.out.println(b/c);

        // % -> Modulus -> It gives you Remainder

        System.out.println(b%c);

        // 2|10|5 -> Quoient
        //  |10|
        // ------
        //   0 -> Remainder

        System.out.println(67%13);

    }
}
