package may.ex_31052025_Operators;

public class Lab025_RelationalOperators {
    public static void main(String[] args) {
        // Relational Operators = Boolean
        // ><, >=, <=, ! --> true or false
        int a = 10;
        int b = 30;
        boolean c = a > b;
        System.out.println(c);

        int age_shruti = 24;
        int age_Hemant = 28;
        boolean result = age_Hemant >= age_shruti;
        System.out.println(result);
    }
}
